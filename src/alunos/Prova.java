package alunos;

public class Prova {
	private int peso;
	private float valor;
	
	public Prova(int peso,float valor) {
		this.peso = peso;
		this.valor = valor;
	}
	
	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public float getValor() {
		return valor;
	}

	public void setValor(float valor) {
		this.valor = valor;
	}

	
}

